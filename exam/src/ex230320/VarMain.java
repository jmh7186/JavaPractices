package ex230320;

public class VarMain {

	public static void main(String[] args) {
		//변수 c에 a문자를 입력하세요.
		char c='a';
		
		//문자열 변수 s에 "hello"라고 입력하세요.
		String s="hello";
		
		//su변수에 숫자 100을 입력하세요.
		int su=100;
		
		//f변수에 4.50의 실수를 입력하세요.
		float f=4.50f;
		
		System.out.println(c);
		System.out.println(s);
		System.out.println(su);
		System.out.println(f);

	}

}
