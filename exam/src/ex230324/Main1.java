package ex230324;

public class Main1 {

	public static void main(String[] args) {
		for(int i=0;i<10;i++) {
			System.out.println("*");
		}
		
		int i=0;
		while(i<10) {
		System.out.println("*");
		i++;
		}
		
		for(i=0;i>-10;i--) {
			System.out.println(i);
		}
		
		i=0;
		while(i>-10) {
		System.out.println("*");
		i--;
		}

	}

}
